def call_api(url, params, timeout=120):
    r = requests.get(url, params=params, timeout=timeout)
    if r.status_code != 200:
        raise RuntimeError(f"API error {r.status_code}: {r.text[:500]}")
    return r.json()


def validate_daily_block(payload, key="daily"):
    if key not in payload:
        raise KeyError(f"'{key}' not found. Payload keys: {list(payload.keys())}")
    if "time" not in payload[key]:
        raise KeyError(f"'time' missing inside payload['{key}']")
    return payload[key]


def safe_mape(y_true, y_pred):
    y_true = np.asarray(y_true)
    y_pred = np.asarray(y_pred)
    mask = np.abs(y_true) > 1e-8
    if mask.sum() == 0:
        return np.nan
    return np.mean(np.abs((y_true[mask] - y_pred[mask]) / y_true[mask])) * 100


def rmse(y_true, y_pred):
    return math.sqrt(mean_squared_error(y_true, y_pred))


def ensure_datetime_index(df, date_col="date"):
    out = df.copy()
    out[date_col] = pd.to_datetime(out[date_col])
    return out.sort_values(date_col).reset_index(drop=True)


def add_fourier_terms(df, date_col="date", period=365.25, order=2, prefix="ann"):
    out = df.copy()
    t = (pd.to_datetime(out[date_col]) - pd.to_datetime(out[date_col]).min()).dt.days.values
    for k in range(1, order + 1):
        out[f"{prefix}_sin_{k}"] = np.sin(2 * np.pi * k * t / period)
        out[f"{prefix}_cos_{k}"] = np.cos(2 * np.pi * k * t / period)
    return out


def compute_api_index(rain_series, decay=0.85):
    """Antecedent Precipitation Index"""
    vals = rain_series.fillna(0).values
    api = np.zeros_like(vals, dtype=float)
    for i in range(1, len(vals)):
        api[i] = vals[i] + decay * api[i-1]
    return api



    test_df  = data.iloc[-test_days:].copy()

    X_train, y_train = train_df[use_features], train_df[target_col].astype(int).values
    X_test, y_test   = test_df[use_features], test_df[target_col].astype(int).values

    # validation chunk for threshold tuning
    val_days = min(120, len(train_df)//5)
    X_fit = X_train.iloc[:-val_days].copy()
    y_fit = y_train[:-val_days]
    X_val = X_train.iloc[-val_days:].copy()
    y_val = y_train[-val_days:]

    models = get_binary_classifiers()
    rows = []
    pred_tables = []
    tuned_models = {}

    for name, mdl in models.items():
        model = clone(mdl)
        model.fit(X_fit, y_fit)

        val_prob = model.predict_proba(X_val)[:, 1]
        th = optimize_threshold(y_val, val_prob, beta=2.0)

        # Refit on full train
        model.fit(X_train, y_train)
        test_prob = model.predict_proba(X_test)[:, 1]
        y_pred_default = (test_prob >= 0.50).astype(int)
        y_pred_tuned = (test_prob >= th["threshold"]).astype(int)

        rows.append({
            "model": name,
            "threshold_used": th["threshold"],
            "Accuracy": accuracy_score(y_test, y_pred_tuned),
            "Balanced_Accuracy": balanced_accuracy_score(y_test, y_pred_tuned),
            "Precision": precision_score(y_test, y_pred_tuned, zero_division=0),
            "Recall": recall_score(y_test, y_pred_tuned, zero_division=0),
            "F1": f1_score(y_test, y_pred_tuned, zero_division=0),
            "ROC_AUC": roc_auc_score(y_test, test_prob) if len(np.unique(y_test)) > 1 else np.nan,
            "PR_AUC": average_precision_score(y_test, test_prob) if len(np.unique(y_test)) > 1 else np.nan,
            "Brier": brier_score_loss(y_test, test_prob),
            "n_positive_test": int(y_test.sum())
        })
        pred_tables.append(pd.DataFrame({
            "date": test_df["date"].values,
            "observed": y_test,
            "probability": test_prob,
            "predicted_default": y_pred_default,
            "predicted_tuned": y_pred_tuned,
            "threshold": th["threshold"],
            "model": name
        }))
        tuned_models[name] = model

    return pd.DataFrame(rows).sort_values(["PR_AUC", "Recall", "ROC_AUC"], ascending=False), pd.concat(pred_tables, ignore_index=True), tuned_models, use_features



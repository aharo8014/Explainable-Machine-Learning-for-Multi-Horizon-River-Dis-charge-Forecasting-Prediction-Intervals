# CELL 20 - SHORT-TERM RECURSIVE PATH FORECAST (OPTIONAL OPERATIONAL MODULE)
# This module creates a daily path forecast up to SHORT_TERM_RECURSIVE_HORIZON days.
# To avoid dependence on same-day flood summary descriptors, it uses the best core model
# for t+1 and updates lags recursively using predicted discharge and future weather.

def fill_future_weather_with_climatology(hist_weather, future_weather, full_horizon=30):
    hist = hist_weather.copy()
    fut = future_weather.copy()
    last_date = fut["date"].max()
    needed = full_horizon - len(fut)
    if needed <= 0:
        return fut.copy()

    monthly_clim = hist.groupby(hist["date"].dt.month).agg({
        "temperature_2m_max": "mean",
        "temperature_2m_min": "mean",
        "precipitation_sum": "mean",
        "rain_sum": "mean",
        "precipitation_hours": "mean",
        "wind_speed_10m_max": "mean",
        "wind_gusts_10m_max": "mean",
        "wind_direction_10m_dominant": "mean",
        "shortwave_radiation_sum": "mean",
        "et0_fao_evapotranspiration": "mean",
        "weather_code": lambda s: s.mode().iloc[0] if not s.mode().empty else s.iloc[-1]
    }).reset_index().rename(columns={"date": "month"})

    new_rows = []
    for i in range(1, needed + 1):
        new_date = last_date + pd.Timedelta(days=i)
        m = new_date.month
        clim = monthly_clim.loc[monthly_clim["date"] == m].iloc[0] if "date" in monthly_clim.columns else monthly_clim.loc[monthly_clim["month"] == m].iloc[0]
        new_rows.append({
            "date": new_date,
            "temperature_2m_max": clim["temperature_2m_max"],
            "temperature_2m_min": clim["temperature_2m_min"],
            "precipitation_sum": clim["precipitation_sum"],
            "rain_sum": clim["rain_sum"],
            "precipitation_hours": clim["precipitation_hours"],
            "wind_speed_10m_max": clim["wind_speed_10m_max"],
            "wind_gusts_10m_max": clim["wind_gusts_10m_max"],
            "wind_direction_10m_dominant": clim["wind_direction_10m_dominant"],
            "shortwave_radiation_sum": clim["shortwave_radiation_sum"],
            "et0_fao_evapotranspiration": clim["et0_fao_evapotranspiration"],
            "weather_code": clim["weather_code"],
        })
    return pd.concat([fut, pd.DataFrame(new_rows)], ignore_index=True)



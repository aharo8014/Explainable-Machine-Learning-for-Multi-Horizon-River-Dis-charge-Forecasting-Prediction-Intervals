def scan_nearby_flow_points(lat, lon, offsets=(-0.08, -0.04, 0.0, 0.04, 0.08), past_days=180):
    rows = []
    for dlat in offsets:
        for dlon in offsets:
            try:
                df = fetch_flood_discharge(lat + dlat, lon + dlon, past_days=past_days, forecast_days=0)
                rows.append({
                    "lat": lat + dlat,
                    "lon": lon + dlon,
                    "n": len(df),
                    "mean_flow": df["river_discharge"].mean(),
                    "std_flow": df["river_discharge"].std(),
                    "max_flow": df["river_discharge"].max(),
                    "nonmissing_ratio": df["river_discharge"].notna().mean(),
                })
            except Exception as e:
                rows.append({
                    "lat": lat + dlat,
                    "lon": lon + dlon,
                    "n": 0,
                    "mean_flow": np.nan,
                    "std_flow": np.nan,
                    "max_flow": np.nan,
                    "nonmissing_ratio": 0,
                    "error": str(e)[:120]
                })
    out = pd.DataFrame(rows)
    score = (
        out["nonmissing_ratio"].fillna(0) * 3 +
        out["std_flow"].fillna(0).rank(pct=True) +
        out["max_flow"].fillna(0).rank(pct=True)
    )
    out["selection_score"] = score
    return out.sort_values("selection_score", ascending=False).reset_index(drop=True)


def add_fourier_terms(df, date_col="date", period=365.25, order=2, prefix="ann"):
    out = df.copy()
    t = (pd.to_datetime(out[date_col]) - pd.to_datetime(out[date_col]).min()).dt.days.values
    for k in range(1, order + 1):
        out[f"{prefix}_sin_{k}"] = np.sin(2 * np.pi * k * t / period)
        out[f"{prefix}_cos_{k}"] = np.cos(2 * np.pi * k * t / period)
    return out


def compute_api_index(rain_series, decay=0.85):
    """Antecedent Precipitation Index"""
    vals = rain_series.fillna(0).values
    api = np.zeros_like(vals, dtype=float)
    for i in range(1, len(vals)):
        api[i] = vals[i] + decay * api[i-1]
    return api



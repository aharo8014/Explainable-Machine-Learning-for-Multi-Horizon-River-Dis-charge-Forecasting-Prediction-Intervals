# CELL 9 - TARGETS (MULTI-HORIZON + RISK REGIMES)
def build_targets(df_feat, horizons=HORIZONS):
    out = df_feat.copy()
    for h in horizons:
        out[f"target_reg_tplus_{h}"] = out["river_discharge"].shift(-h)

    # Primary next-day risk thresholds based on future discharge
    q_watch   = out["target_reg_tplus_1"].quantile(Q_WATCH)
    q_warning = out["target_reg_tplus_1"].quantile(Q_WARNING)
    q_extreme = out["target_reg_tplus_1"].quantile(Q_EXTREME)

    out["target_cls_highrisk_tplus_1"] = (out["target_reg_tplus_1"] >= q_warning).astype(int)

    def regime_mapper(x):
        if pd.isna(x):
            return np.nan
        if x < q_watch:
            return 0  # Normal
        elif x < q_warning:
            return 1  # Watch
        elif x < q_extreme:
            return 2  # Warning
        else:
            return 3  # Extreme

    out["target_regime_tplus_1"] = out["target_reg_tplus_1"].apply(regime_mapper)
    thresholds = {
        "watch": float(q_watch),
        "warning": float(q_warning),
        "extreme": float(q_extreme),
    }
    return out, thresholds



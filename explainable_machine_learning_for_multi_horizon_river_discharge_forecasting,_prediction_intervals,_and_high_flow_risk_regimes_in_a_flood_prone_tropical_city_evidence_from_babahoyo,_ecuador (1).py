# CELL 8 - ADVANCED FEATURE ENGINEERING (LEAKAGE-AWARE)
def engineer_features(df):
    data = ensure_datetime_index(df)
    out = data.copy()

    # Calendar / seasonality
    out["year"] = out["date"].dt.year
    out["month"] = out["date"].dt.month
    out["day"] = out["date"].dt.day
    out["dayofyear"] = out["date"].dt.dayofyear
    out["weekofyear"] = out["date"].dt.isocalendar().week.astype(int)
    out["quarter"] = out["date"].dt.quarter
    out["is_wet_season_ec"] = out["month"].isin([1,2,3,4]).astype(int)
    out = add_fourier_terms(out, "date", period=365.25, order=3, prefix="annual")

    # Core weather engineering
    out["temp_range"] = out["temperature_2m_max"] - out["temperature_2m_min"]
    out["water_balance_proxy"] = out["precipitation_sum"] - out["et0_fao_evapotranspiration"]
    out["rain_intensity_proxy"] = out["precipitation_sum"] / (out["precipitation_hours"].replace(0, np.nan))
    out["rain_intensity_proxy"] = out["rain_intensity_proxy"].replace([np.inf, -np.inf], np.nan).fillna(0)
    out["gust_factor"] = out["wind_gusts_10m_max"] / out["wind_speed_10m_max"].replace(0, np.nan)
    out["gust_factor"] = out["gust_factor"].replace([np.inf, -np.inf], np.nan).fillna(0)

    # Antecedent precipitation index
    out["api_085"] = compute_api_index(out["precipitation_sum"], decay=0.85)
    out["api_093"] = compute_api_index(out["precipitation_sum"], decay=0.93)

    # Wet-spell and dry-spell counters
    wet = (out["precipitation_sum"] > 1.0).astype(int)
    dry = (out["precipitation_sum"] <= 1.0).astype(int)
    wet_streak, dry_streak = [], []
    wc = dc = 0
    for w, d in zip(wet, dry):
        wc = wc + 1 if w == 1 else 0
        dc = dc + 1 if d == 1 else 0
        wet_streak.append(wc)
        dry_streak.append(dc)
    out["wet_streak"] = wet_streak
    out["dry_streak"] = dry_streak

    # Heavy rainfall flags
    out["heavy_rain_25mm"] = (out["precipitation_sum"] >= 25).astype(int)
    out["heavy_rain_50mm"] = (out["precipitation_sum"] >= 50).astype(int)
    out["very_heavy_rain_75mm"] = (out["precipitation_sum"] >= 75).astype(int)

    # Weather rolling windows
    weather_windows = [1, 3, 5, 7, 14, 21, 30, 60]
    for w in weather_windows:
        out[f"rain_sum_{w}d"] = out["precipitation_sum"].rolling(w).sum()
        out[f"rain_mean_{w}d"] = out["precipitation_sum"].rolling(w).mean()
        out[f"rain_max_{w}d"] = out["precipitation_sum"].rolling(w).max()
        out[f"rain_std_{w}d"] = out["precipitation_sum"].rolling(w).std()
        out[f"api085_mean_{w}d"] = out["api_085"].rolling(w).mean()
        out[f"wet_days_{w}d"] = wet.rolling(w).sum()
        out[f"heavy25_count_{w}d"] = out["heavy_rain_25mm"].rolling(w).sum()
        out[f"heavy50_count_{w}d"] = out["heavy_rain_50mm"].rolling(w).sum()

    # Current flow and lag features
    out["flow_diff_1"] = out["river_discharge"].diff(1)
    out["flow_diff_7"] = out["river_discharge"].diff(7)
    out["flow_pctchg_1"] = out["river_discharge"].pct_change(1)
    out["flow_pctchg_7"] = out["river_discharge"].pct_change(7)

    for lag in [1,2,3,5,7,10,14,21,30,45,60]:
        out[f"flow_lag_{lag}"] = out["river_discharge"].shift(lag)
        out[f"rain_lag_{lag}"] = out["precipitation_sum"].shift(lag)
        out[f"tempmax_lag_{lag}"] = out["temperature_2m_max"].shift(lag)

    # Flow rolling features (strict operational set uses only river_discharge)
    flow_windows = [3,5,7,10,14,21,30,45,60,90]
    for w in flow_windows:
        roll = out["river_discharge"].rolling(w)
        out[f"flow_mean_{w}d"] = roll.mean()
        out[f"flow_std_{w}d"]  = roll.std()
        out[f"flow_min_{w}d"]  = roll.min()
        out[f"flow_max_{w}d"]  = roll.max()
        out[f"flow_med_{w}d"]  = roll.median()
        out[f"flow_q10_{w}d"]  = roll.quantile(0.10)
        out[f"flow_q90_{w}d"]  = roll.quantile(0.90)
        out[f"flow_slope_{w}d"] = out["river_discharge"].diff().rolling(w).mean()

    # Rolling anomaly scores
    out["flow_z_30"] = (out["river_discharge"] - out["flow_mean_30d"]) / out["flow_std_30d"].replace(0, np.nan)
    out["flow_z_60"] = (out["river_discharge"] - out["flow_mean_60d"]) / out["flow_std_60d"].replace(0, np.nan)

    # Hydroclimatic interaction terms
    out["rain3_flow7_interaction"] = out["rain_sum_3d"] * out["flow_mean_7d"]
    out["rain7_flow14_interaction"] = out["rain_sum_7d"] * out["flow_mean_14d"]
    out["balance_flow_interaction"] = out["water_balance_proxy"] * out["river_discharge"]
    out["radiation_rain_interaction"] = out["shortwave_radiation_sum"] * out["precipitation_sum"]
    out["wetness_velocity_interaction"] = out["api_093"] * out["wind_speed_10m_max"]

    # OPTIONAL benchmark-only same-day flood descriptors
    # These can be informative, but they are NOT used in the leakage-safe operational model.
    if {"river_discharge_mean", "river_discharge_median", "river_discharge_max", "river_discharge_min", "river_discharge_p25", "river_discharge_p75"}.issubset(out.columns):
        out["flow_spread_same_day"] = out["river_discharge_p75"] - out["river_discharge_p25"]
        out["flow_mean_gap_same_day"] = out["river_discharge"] - out["river_discharge_mean"]

    return out



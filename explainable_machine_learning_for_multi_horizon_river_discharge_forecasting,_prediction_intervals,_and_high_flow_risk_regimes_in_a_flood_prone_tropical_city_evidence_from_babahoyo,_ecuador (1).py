@dataclass
class SplitConfig:
    min_train_size: int = MIN_TRAIN_DAYS
    test_size: int = 90
    step_size: int = STEP_DAYS
    n_splits: int = N_BACKTEST_SPLITS


def rolling_origin_splits(n_samples, cfg: SplitConfig):
    """
    Rolling-origin splits with growing train window.
    Returns list of (train_idx, test_idx).
    """
    splits = []
    train_end = cfg.min_train_size
    for _ in range(cfg.n_splits):
        test_start = train_end
        test_end = test_start + cfg.test_size
        if test_end > n_samples:
            break
        train_idx = np.arange(0, train_end)
        test_idx  = np.arange(test_start, test_end)
        splits.append((train_idx, test_idx))
        train_end += cfg.step_size
    return splits


def optimize_threshold(y_true, y_prob, beta=2.0):
    thresholds = np.unique(np.round(np.linspace(0.05, 0.95, 181), 4))
    best = {"threshold": 0.5, "f_beta": -1}
    for t in thresholds:
        y_hat = (y_prob >= t).astype(int)
        p = precision_score(y_true, y_hat, zero_division=0)
        r = recall_score(y_true, y_hat, zero_division=0)
        if p == 0 and r == 0:
            fbeta = 0
        else:
            fbeta = (1 + beta**2) * (p * r) / ((beta**2 * p) + r + 1e-12)
        if fbeta > best["f_beta"]:
            best = {"threshold": t, "f_beta": fbeta, "precision": p, "recall": r}
    return best


# CELL 20 - SHORT-TERM RECURSIVE PATH FORECAST (OPTIONAL OPERATIONAL MODULE)
# This module creates a daily path forecast up to SHORT_TERM_RECURSIVE_HORIZON days.
# To avoid dependence on same-day flood summary descriptors, it uses the best core model
# for t+1 and updates lags recursively using predicted discharge and future weather.

def fill_future_weather_with_climatology(hist_weather, future_weather, full_horizon=30):
    hist = hist_weather.copy()
    fut = future_weather.copy()
    last_date = fut["date"].max()
    needed = full_horizon - len(fut)
    if needed <= 0:
        return fut.copy()

    monthly_clim = hist.groupby(hist["date"].dt.month).agg({
        "temperature_2m_max": "mean",
        "temperature_2m_min": "mean",
        "precipitation_sum": "mean",
        "rain_sum": "mean",
        "precipitation_hours": "mean",
        "wind_speed_10m_max": "mean",
        "wind_gusts_10m_max": "mean",
        "wind_direction_10m_dominant": "mean",
        "shortwave_radiation_sum": "mean",
        "et0_fao_evapotranspiration": "mean",
        "weather_code": lambda s: s.mode().iloc[0] if not s.mode().empty else s.iloc[-1]
    }).reset_index().rename(columns={"date": "month"})

    new_rows = []
    for i in range(1, needed + 1):
        new_date = last_date + pd.Timedelta(days=i)
        m = new_date.month
        clim = monthly_clim.loc[monthly_clim["date"] == m].iloc[0] if "date" in monthly_clim.columns else monthly_clim.loc[monthly_clim["month"] == m].iloc[0]
        new_rows.append({
            "date": new_date,
            "temperature_2m_max": clim["temperature_2m_max"],
            "temperature_2m_min": clim["temperature_2m_min"],
            "precipitation_sum": clim["precipitation_sum"],
            "rain_sum": clim["rain_sum"],
            "precipitation_hours": clim["precipitation_hours"],
            "wind_speed_10m_max": clim["wind_speed_10m_max"],
            "wind_gusts_10m_max": clim["wind_gusts_10m_max"],
            "wind_direction_10m_dominant": clim["wind_direction_10m_dominant"],
            "shortwave_radiation_sum": clim["shortwave_radiation_sum"],
            "et0_fao_evapotranspiration": clim["et0_fao_evapotranspiration"],
            "weather_code": clim["weather_code"],
        })
    return pd.concat([fut, pd.DataFrame(new_rows)], ignore_index=True)


def recursive_daily_forecast(df_hist_base, future_weather, horizon_days=SHORT_TERM_RECURSIVE_HORIZON):
    target_col = "target_reg_tplus_1"
    best_model_name = (summary_reg.query("feature_set == 'core_operational' and horizon_days == 1")
                       .sort_values("R2", ascending=False).iloc[0]["model"])

    data_base = df_hist_base.copy()
    future_weather = fill_future_weather_with_climatology(df_weather_hist, future_weather, full_horizon=horizon_days)
    fitted_model, use_features, full_data = fit_direct_model_full_history(df_model, core_feature_cols, target_col, best_model_name)

    rec_rows = []
    work = data_base.copy()
    future_weather = future_weather.iloc[:horizon_days].copy().reset_index(drop=True)

    for i in range(len(future_weather)):
        wrow = future_weather.iloc[i].to_dict()
        next_date = pd.to_datetime(wrow["date"])

        # Append future weather row with placeholder discharge.
        new_row = {c: np.nan for c in work.columns}
        for c, v in wrow.items():
            if c in new_row:
                new_row[c] = v
        new_row["date"] = next_date
        work = pd.concat([work, pd.DataFrame([new_row])], ignore_index=True)

        # Fill discharge placeholder with last observed/predicted discharge before engineering features
        work.loc[work.index[-1], "river_discharge"] = work["river_discharge"].ffill().iloc[-1]

        eng = engineer_features(work)
        eng, _ = build_targets(eng)

        latest = eng.iloc[[-1]].copy()
        X_latest = latest[use_features]

        if best_model_name == "Persistence_t-1":
            pred = float(X_latest["flow_lag_1"].iloc[0])
        elif best_model_name == "SeasonalPersistence_t-7":
            pred = float(X_latest["flow_lag_7"].iloc[0])
        else:
            pred = float(fitted_model.predict(X_latest)[0])

        # Write prediction back into base work table for recursive propagation
        work.loc[work.index[-1], "river_discharge"] = pred
        rec_rows.append({
            "date": next_date,
            "predicted_discharge_recursive": pred,
            "source": "ML_recursive_tplus1"
        })

    return pd.DataFrame(rec_rows)


